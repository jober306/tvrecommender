package util;

/**
 * A class that holds an integer value. It is useful when a method needs to
 * output multiple object.
 * 
 * @author Jonathan Bergeron
 *
 */
public class IntHolder {
	public int value;

	public IntHolder() {
		value = 0;
	}

	public IntHolder(int value) {
		this.value = value;
	}
}
